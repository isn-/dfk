size_t dfk_urldecode(char* buf, size_t size, char* out, size_t* byteswritten)
{
  assert(buf || !size);
  assert(out || !size);
  assert(byteswritten);

  char* end = buf + size;
  char* initial_out = out;
  while (buf != end) {
    if (*buf == '%') {
      if (buf + 2 >= end) {
        break;
      }
      uint8_t hi = hex_to_byte[(unsigned char) buf[1]];
      uint8_t lo = hex_to_byte[(unsigned char) buf[2]];
      if (hi & 0xF0 || lo & 0xF0) {
        break;
      }
      *out++ = (hi << 4) | lo;
      buf += 3;
    } else {
      *out++ = *buf++;
    }
  }
  *byteswritten = out - initial_out;
  return buf - end + size;
}


